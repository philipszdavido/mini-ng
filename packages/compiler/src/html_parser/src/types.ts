export enum TokenNodeType {
  Node = "Node",
  Text = "Text",
}

export interface Attributes {
  name: string;
  value?: string | null;
}

export interface Token {
  id: string;
  index: number;
  name: string;
  startTag?: boolean;
  endTag?: boolean;
  attributes?: Attributes[];
  type: TokenNodeType;
}

export type AttrMap = Record<string, string>;

export type NodeType =
  | { kind: "text"; value: string }
  | { kind: "element"; value: ElementData };

export interface Node {
  id: string;
  children: Node[];
  nodeType: NodeType;
}

export interface ElementData {
  tagName: HTMLTagName;
  attributes: AttrMap;
}

